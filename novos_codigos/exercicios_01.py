# Algoritmo em Python utilizando variáveis e listas

# Definindo uma variável
nome = "João"
idade = 25

# Definindo uma lista
hobbies = ["leitura", "futebol", "programação"]

# Exibindo informações
print(f"Nome: {nome}")
print(f"Idade: {idade}")
print("Hobbies:")

# Iterando sobre a lista e exibindo cada hobby
for hobby in hobbies:
    print(f"- {hobby}")